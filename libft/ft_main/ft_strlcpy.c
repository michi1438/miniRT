#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	ind;

	ind = 0;
	i = 0;
	while (src[ind] != '\0')
		ind++;
	if (size == 0)
		return (ind);
	else
	{	
		while (i < (size - 1) && src[i] != '\0')
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	return (ind);
}
